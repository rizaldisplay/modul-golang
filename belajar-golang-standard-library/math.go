package main

import (
	"fmt"
	"math"
)

func main() {
	fmt.Println("math.Ceil:", math.Ceil(1.40))
	fmt.Println("math.Floor:", math.Floor(1.40))
	fmt.Println("math.Round:", math.Round(1.40))
	fmt.Println("math.Max:", math.Max(1.40, 1.50))
	fmt.Println("math.Min:", math.Min(1.40, 1.50))
	fmt.Println("math.Pow:", math.Pow(2, 3))
	fmt.Println("math.Sqrt:", math.Sqrt(16))
}
